type TranslationTree = typeof translations.ar;

export function getTranslation(language: AppLanguage, key: string) {
  const tree = translations[language] as TranslationTree;
  const fallback = translations.ar as TranslationTree;
  const segments = key.split('.');

  const resolve = (source: any) =>
    segments.reduce((value, segment) => (value && value[segment] ? value[segment] : undefined), source);

  return resolve(tree) || resolve(fallback) || key;
}

