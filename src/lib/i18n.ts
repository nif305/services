function preserveOuterWhitespace(source: string, translated: string) {
  const leading = source.match(/^\s*/)?.[0] || '';
  const trailing = source.match(/\s*$/)?.[0] || '';
  return `${leading}${translated}${trailing}`;
}

function translateCoreText(source: string, dictionary: Record<string, string>) {
  const trimmed = source.trim();
  if (!trimmed) return source;

  const exact = dictionary[trimmed];
  if (exact) return preserveOuterWhitespace(source, exact);

  const pageMatch = trimmed.match(/^الصفحة\s+(.+)\s+من\s+(.+)$/);
  if (pageMatch) {
    return preserveOuterWhitespace(source, `Page ${pageMatch[1]} of ${pageMatch[2]}`);
  }

  const totalInViewMatch = trimmed.match(/^إجمالي\s+(.+)\s+في\s+هذا\s+العرض:\s*(.+)$/);
  if (totalInViewMatch) {
    const label = dictionary[totalInViewMatch[1]] || totalInViewMatch[1];
    return preserveOuterWhitespace(source, `Total ${label} in this view: ${totalInViewMatch[2]}`);
  }

  const colonMatch = trimmed.match(/^([^:：]+):\s*(.+)$/);
  if (colonMatch) {
    const label = dictionary[colonMatch[1].trim()];
    if (label) return preserveOuterWhitespace(source, `${label}: ${colonMatch[2]}`);
  }

  const parentheticalCount = trimmed.match(/^(.+)\s+\((\d+)\)$/);
  if (parentheticalCount) {
    const label = dictionary[parentheticalCount[1].trim()];
    if (label) return preserveOuterWhitespace(source, `${label} (${parentheticalCount[2]})`);
  }

  return source;
}

export function translateStaticUiText(source: string, language: AppLanguage) {
  if (language === 'ar') {
    return translateCoreText(source, STATIC_UI_AR);
  }

  return translateCoreText(source, STATIC_UI_EN);
}
